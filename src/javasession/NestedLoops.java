package javasession;

public class NestedLoops {

	public static void main(String[] args) {
		
		//outer loop
		for(int i = 0; i<=9; i++) {
			
			//inner loop
			for(int j = 0; j<=9; j++) {
			
				System.out.print(i+""+j+" ");
		}
			
			System.out.println();
			System.out.println("----------------------------");
		}	
		
		
		
		
		
		
		
		
		
		

	}
}
